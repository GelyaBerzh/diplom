fun toGrams(quantity: Double, unit: String, ingredientName: String): Double {
    val normalizedUnit = unit.trim().lowercase()
    return when (normalizedUnit) {
        "г", "g" -> quantity
        "кг", "kg" -> quantity * 1000.0
        "мл", "ml" -> quantity // условно 1 мл = 1 г для жидких
        "л", "l" -> quantity * 1000.0
        "шт", "pcs", "pc" -> {
            // пример: для яиц считаем 1 шт ≈ 50 г
            if (ingredientName.lowercase().contains("яйц")) quantity * 50.0 else quantity * 50.0
        }
        "ч.л.", "tsp" -> quantity * 5.0
        "ст.л.", "tbsp" -> quantity * 15.0
        "щепотка", "pinch" -> quantity * 0.3
        else -> quantity
    }
}




